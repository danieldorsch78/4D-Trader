        id = id, name = name, description = description,
        items = assets.mapIndexed { i, a ->
            WatchlistItem(a.symbol, a, i, addedTimestamp = Clock.System.now().toEpochMilliseconds())
        },
        sortOrder = sortOrder, isDefault = isDefault
    )

